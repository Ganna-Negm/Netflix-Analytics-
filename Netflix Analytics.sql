create database NetflixAnalytics
use NetflixAnalytics
Go
create table Content(
Content_id int primary key,
Title varchar(100) not null,
Description varchar(1000),
Type varchar(30) not null default 'Movie',
Maturity_rate varchar(20) not null,
Date_added date,
Duration_minutes int,
Release_year int
)
create table Country(
Country_id int primary key,
Country_name varchar(100),
)
create table Content_Country(
country_Id int foreign key(country_Id) REFERENCES Country(Country_id),
content_Id int foreign key(content_Id) REFERENCES Content(Content_id)
primary key(country_Id,content_Id),
)
create table Genre(
Genre_id int primary key,
Genre_name varchar(100)
)
create table Content_Genre(
Content_Id int foreign key(Content_Id) REFERENCES Content(Content_id),
genre_Id int foreign key(genre_Id) REFERENCES Genre(Genre_id)
primary key(Content_Id,genre_Id),
)
create table Users(
User_id int primary key,
Country_id int foreign key(Country_id) REFERENCES Country(Country_id),
Gender varchar(20) default'male',
DOB date,
Subscription_type varchar(100),
Subscription_start_date date
)
create table Watch_history(
Watch_id int primary key,
user_id int foreign key(user_id) REFERENCES Users(User_id),
Content_id int foreign key(Content_id) REFERENCES Content(Content_id),
Watch_date date,
Watch_duration time,
Compeleted varchar(10) default'yes'
)
create table Rating(
Rating_id int primary key,
user_id int foreign key(user_id) REFERENCES Users(User_id),
content_id int foreign key(content_id) REFERENCES Content(Content_id ),
Rate_score int not null,
Date date
)
create table Supscription_plan(
Plan_id int primary key,
Plan_name varchar(100),
Monthly_price smallmoney,
)
alter table Users
add Plan_id int foreign key(Plan_id) REFERENCES Supscription_plan(Plan_id) 
alter table Users
drop column Subscription_type
alter table Watch_history
drop column Watch_duration
alter table Watch_history
add Watch_duration int 
alter table Rating
add CONSTRAINT Chk_Rating 
check(Rate_score between 1 and 5)
